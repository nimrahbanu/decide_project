    {
        $request->validate([
            'country' => 'required',
           
        ]);
        $data = $request->all();
        $status = Country::create($data);
        if($status){
            request()->session()->flash('success', 'Country  Form Created Successfully !!');
        }else{
            request()->session()->flash('error', 'Country Form not created !!');
        }
        return redirect()->back();
    }

